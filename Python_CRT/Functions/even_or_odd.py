''' 
program to check whether user given integer is even or odd number using functions
'''
def Even_Odd(Num):
    if Num%2==0:
        print(f"{Num} is Even")
    else:
        print(f"{Num} is Odd")
Even_Odd(10)
Even_Odd(15)
Even_Odd(25)
Even_Odd(7)
Even_Odd(36)
Even_Odd(28)
Even_Odd(18)